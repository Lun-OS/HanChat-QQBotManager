// SimpleImageProcessor 简单的图像处理器，提供基础的图像操作功能
type SimpleImageProcessor struct {
	Canvas *image.RGBA
	Font   *truetype.Font
}

// NewImageProcessor 创建新的图像处理器
func NewImageProcessor(imageData []byte) (*SimpleImageProcessor, error) {
	// 解码图片数据
	img, _, err := image.Decode(bytes.NewReader(imageData))
	if err != nil {
		return nil, fmt.Errorf("图片解码失败: %v", err)
	}

	// 创建画布（RGBA格式）
	bounds := img.Bounds()
	canvas := image.NewRGBA(bounds)
	draw.Draw(canvas, bounds, img, bounds.Min, draw.Src)

	processor := &SimpleImageProcessor{Canvas: canvas}

	// 尝试加载字体文件
	fontPaths := []string{
		filepath.Join("config", "Font-ttf.ttf"),
		filepath.Join("config", "font-ttf.ttf"),
		filepath.Join("config", "font.ttf"),
	}

	var fontData []byte
	var font *truetype.Font
	var fontLoaded bool

	// 尝试多个可能的字体文件路径
	for _, fontPath := range fontPaths {
		fmt.Printf("尝试加载字体文件: %s\n", fontPath)
		if _, statErr := os.Stat(fontPath); statErr == nil {
			fontData, err = ioutil.ReadFile(fontPath)
			if err != nil {
				fmt.Printf("读取字体文件失败: %s, 错误: %v\n", fontPath, err)
				continue
			}

			font, err = truetype.Parse(fontData)
			if err != nil {
				fmt.Printf("解析字体文件失败: %s, 错误: %v\n", fontPath, err)
				continue
			}

			// 成功加载字体
			processor.Font = font
			fontLoaded = true
			fmt.Printf("成功加载字体文件: %s\n", fontPath)
			break
		} else {
			fmt.Printf("字体文件不存在: %s, 错误: %v\n", fontPath, statErr)
		}
	}

	if !fontLoaded {
		fmt.Printf("警告: 无法加载任何字体文件，文本标注功能将不可用\n")
		// 不返回错误，允许在没有字体的情况下使用其他功能
	}

	return processor, nil
}

// DrawRectangle 绘制矩形框
func (p *SimpleImageProcessor) DrawRectangle(x1, y1, x2, y2 int, r, g, b, a uint8, thickness int) {
	color := color.RGBA{r, g, b, a}

	// 绘制上边框
	for y := y1; y < y1+thickness; y++ {
		for x := x1; x <= x2; x++ {
			p.Canvas.Set(x, y, color)
		}
	}
	// 绘制下边框
	for y := y2 - thickness; y < y2; y++ {
		for x := x1; x <= x2; x++ {
			p.Canvas.Set(x, y, color)
		}
	}
	// 绘制左边框
	for x := x1; x < x1+thickness; x++ {
		for y := y1; y <= y2; y++ {
			p.Canvas.Set(x, y, color)
		}
	}
	// 绘制右边框
	for x := x2 - thickness; x < x2; x++ {
		for y := y1; y <= y2; y++ {
			p.Canvas.Set(x, y, color)
		}
	}
}

// DrawFilledRect 绘制填充矩形（支持透明度）
func (p *SimpleImageProcessor) DrawFilledRect(x1, y1, x2, y2 int, r, g, b, a uint8) {
	// 如果alpha值为255（完全不透明），直接绘制
	if a == 255 {
		color := color.RGBA{r, g, b, a}
		for y := y1; y < y2; y++ {
			for x := x1; x < x2; x++ {
				p.Canvas.Set(x, y, color)
			}
		}
		return
	}

	// 否则进行透明度混合
	alphaRatio := float64(a) / 255.0
	for y := y1; y < y2; y++ {
		for x := x1; x < x2; x++ {
			// 获取原始像素颜色
			original := p.Canvas.RGBAAt(x, y)

			// 计算混合后的颜色
			newR := uint8(float64(original.R)*(1-alphaRatio) + float64(r)*alphaRatio)
			newG := uint8(float64(original.G)*(1-alphaRatio) + float64(g)*alphaRatio)
			newB := uint8(float64(original.B)*(1-alphaRatio) + float64(b)*alphaRatio)

			// 设置混合后的颜色（保持完全不透明）
			p.Canvas.SetRGBA(x, y, color.RGBA{newR, newG, newB, 255})
		}
	}
}

// DrawText 绘制文本（使用TTF字体）
func (p *SimpleImageProcessor) DrawText(x, y int, text string, r, g, b, a uint8, fontSize int) {
	// 如果没有加载字体，则使用简单的像素点阵字体
	if p.Font == nil {
		p.drawTextWithPixelFont(x, y, text, r, g, b, a, fontSize)
		return
	}

	// 使用TTF字体绘制
	c := freetype.NewContext()
	c.SetDPI(72)
	c.SetFont(p.Font)
	c.SetFontSize(float64(fontSize))
	c.SetClip(p.Canvas.Bounds())
	c.SetDst(p.Canvas)
	c.SetSrc(image.NewUniform(color.RGBA{r, g, b, a}))

	// 计算文本位置 - 调整Y坐标以确保文本与像素字体位置一致
	// 在freetype中，Y坐标是文本基线的位置，而我们的像素字体是从顶部开始绘制的
	// 所以需要将Y坐标向下调整，使文本基线位于y+fontSize的位置
	// 这样可以使TTF字体的顶部大约位于y位置，与像素字体保持一致
	pt := freetype.Pt(x, y+fontSize*2)

	// 绘制文本
	_, err := c.DrawString(text, pt)
	if err != nil {
		// 如果TTF字体绘制失败，回退到像素点阵字体
		p.drawTextWithPixelFont(x, y, text, r, g, b, a, fontSize)
	}
}

