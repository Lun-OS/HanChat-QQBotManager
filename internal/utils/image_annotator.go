// GetImageData 获取处理后的图像数据
func (p *SimpleImageProcessor) GetImageData() ([]byte, error) {
	// 编码为JPEG格式
	var buf bytes.Buffer
	err := jpeg.Encode(&buf, p.Canvas, &jpeg.Options{Quality: 90})
	if err != nil {
		return nil, fmt.Errorf("JPEG编码失败: %v", err)
	}

	return buf.Bytes(), nil
}
